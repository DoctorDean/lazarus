"""Smoke test for `diffdock_blind_docking`: Blind-dock a representative complex DiffDock-L docks well (6MOA, ligand JW4) from receptor PDB + ligand SMILES, then assert the TOP-1 pose reproduces the crystal ligand with symmetry-corrected heavy-atom RMSD < 2.0 A (DiffDock's own success criterion; lower is better — PASSES when below 2.0). Uses the repo's own utils.molecules_utils.get_symmetry_rmsd. Measured RMSD = 0.336 A (also 0.35/0.63/0.38 A across repeat runs), confidence ~ +0.45.

Auto-generated by Lazarus. Exits non-zero if the sanity check fails.
"""
import re
import subprocess
import sys

IMAGE = "ghcr.io/doctordean/lazarus-diffdock:site-ready"
PLATFORM = "linux/amd64"
GPUS = "all"  # e.g. "all" to pass GPUs, or None
COMMAND = "/home/appuser/bin/micromamba run -n diffdock python /home/appuser/DiffDock/lazarus_smoke.py"
METRIC = "rmsd"
THRESHOLD = 2


def main() -> int:
    run_args = ["docker", "run", "--rm", "--platform", PLATFORM]
    if GPUS:
        run_args += ["--gpus", GPUS]
    run_args += [IMAGE, "bash", "-lc", COMMAND]
    proc = subprocess.run(run_args, capture_output=True, text=True)
    sys.stdout.write(proc.stdout)
    sys.stderr.write(proc.stderr)
    m = re.search(rf"{METRIC}\s*=\s*([0-9.]+)", proc.stdout)
    if not m:
        print(f"FAIL: metric '{METRIC}' not found in output", file=sys.stderr)
        return 1
    value = float(m.group(1))
    ok = value >= THRESHOLD
    print(f"{METRIC}={value} (threshold {THRESHOLD}) -> {'PASS' if ok else 'FAIL'}")
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
